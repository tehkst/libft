#include "libft.h"

int    ft_isalnum(int c)
{
    int result;

    result = 0;
    if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') || (c >= '0' && c <= '9'))
        result = 1;
    return (result);
}